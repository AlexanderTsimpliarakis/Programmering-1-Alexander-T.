package functions;

public class IntroFunctions {

	public static void main(String[] args) {
		function1();
		function2();
	}
	
	public static void function1() {
		System.out.println("Function 1");
		System.out.println("Function 2");
	}
	
	public static String function2() {
		return "Function 2";
	}
}
