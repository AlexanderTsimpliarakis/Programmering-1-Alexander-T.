package IterationsSatser;

public class ThreeAndFive {

	public static void main(String[] args) {
		
		for(int i = 3; i <=100; i += 3) {
			System.out.println(i);
		}
		for(int i = 5; i <=100; i += 5) {
			System.out.println(i);
		}
		for(int i = 15; i <=100; i += 15) {
			System.out.println(i);
		}

	}

}
