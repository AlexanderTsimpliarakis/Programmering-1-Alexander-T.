package Printing;

public class NurseryRhyme {

	public static void main(String[] args) {
		System.out.println("Doe a deer, a female deer,");
		System.out.println("Ray a drop of golden sun,");
		System.out.println("Me a name i call myself,");
		System.out.println("Far a long long way to run,");
		System.out.println("Sew a needle pulling thread,");
		System.out.println("La a note to follow so,");
		System.out.println("Te a drink with jam and bread,");
		System.out.println("And all leads back to doe.");

	}

}
